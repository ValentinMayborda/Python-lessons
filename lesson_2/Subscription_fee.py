print('Програма розраховує абонплату за показниками лічильника електроенергії.')

previous = int(input('Введіть попередні показники лічильника (кВт):'))
current = int(input('Введіть теперішні показники лічильника (кВт):'))
tariff = float(input('Введіть Ваш тариф(грн/кВт*год):'))
pay = round((current - previous) * tariff, 2)

print(f'Вам необхідно сплатити за {current - previous} кВт по тарифу {tariff} грн/кВт*год: {int(pay)} грн.\
{int((pay * 100) % 100)} коп.')
