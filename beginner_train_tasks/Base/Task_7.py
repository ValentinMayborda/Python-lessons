# Програма приймає дві строки та повідомляє чи є друга підстрокою першої
# (наприклад для s1="Я пишу на Python", строка s2="Python" буде підстрокой, а s2="Javascript" не буде)
print('Програма приймає дві строки та повідомляє чи є друга підстрокою першої ')

text1 = input('Введіть першу строку\n>>> ')
text2 = input('Введіть другу строку\n>>> ')
if text2 in text1:
    print(f'{text2} є підстрокою {text1}')
else:
    print('Друга строка не є підстрокою пешої.')
