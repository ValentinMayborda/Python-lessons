# Програма приймає два числа та знаходить процентну частку одного від іншого (n1=40, n2=10, percent=25%)
print('Програма приймає два числа та знаходить процентну частку одного від іншого')

a, b = int(input('Введіть перше число: ')), int(input('Введіть друге число: '))
if a > b:
    print(f'n1={a}, n2={b}, percent={b / a * 100}%')
elif b > a:
    print(f'n1={a}, n2={b}, percent={a / b * 100}%')
else:
    print(f'n1={a}, n2={b}, percent={a / b * 100}%')
