# Програма приймає два числа та повідомляє яке з них менше та у скільки разів (20 менше 100 у 5 разів).
print('Програма приймає два числа та повідомляє, яке з них менше та у скільки разів.')

a, b = int(input('Введіть перше число: ')), int(input('Введіть друге число: '))
if a > b:
    print(f'{b} менше {a} у {a // b} разів')
elif b > a:
    print(f'{a} менше {b} у {b // a} разів')
else:
    print('Числа рівні!')
