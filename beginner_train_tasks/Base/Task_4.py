# Програма приймає число та повідомляє чи є воно паліндромом (тобто дзеркальним,
# наприклад 35653 - паліндром, а 35654 - ні)
print('Програма приймає число та повідомляє чи є воно паліндромом')
number = input('Введіть число: ')
palindrome = number[::-1]
if number == palindrome:
    print(f'Число {number} є паліндромом! ')
else:
    print('Не паліндром!')
